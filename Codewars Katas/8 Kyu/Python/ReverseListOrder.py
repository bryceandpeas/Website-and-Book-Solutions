'''

Codewars Kata - 8 Kyu - Reverse List Order

Description:

In this kata you will create a function that takes in a list and returns a list with the reverse order.

Examples

reverse_list([1,2,3,4]) == [4,3,2,1]
reverse_list([3,1,5,4]) == [4,5,1,3]

'''

def reverse_list(l):
	l.reverse()
	return (l)